public class N2ListNode {
    int val;
    N2ListNode next;

    N2ListNode() {
    }

    N2ListNode(int val) {
        this.val = val;
    }

    N2ListNode(int val, N2ListNode next) {
        this.val = val;
        this.next = next;
    }
}
